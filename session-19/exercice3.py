
import http.client
import json

user = input("Write the user to search: ")

# -- API information
HOSTNAME = "api.github.com"
ENDPOINT1 = "/users/"
ENDPOINT2 = "/repos"
GITHUB_ID = "{}".format(user)
METHOD = "GET"

# -- Here we can define special headers if needed
headers = {'User-Agent': 'http-client'}

# -- Connect to the server
# -- NOTICE it is an HTTPS connection!
# -- If we do not specify the port, the standar one
# -- will be used
conn = http.client.HTTPSConnection(HOSTNAME)

# -- Send the request. No body (None)
# -- Use the defined headers
conn.request(METHOD, ENDPOINT1 + GITHUB_ID, None, headers)

# -- Wait for the server's response
r1 = conn.getresponse()

# -- Print the status
print()
print("Response received: ", end='')
print(r1.status, r1.reason)

# -- Read the response's body and close
# -- the connection
text_json = r1.read().decode("utf-8")
conn.close()

# -- Optionally you can print the
# -- received json file for testing
# print(text_json)

# -- Generate the object from the json file
user = json.loads(text_json)

# -- Get some data
name = user['name']
bio = user['bio']
nrepos = user['public_repos']

print()
print("Real Name: {}".format(name))
print("Repos: {}".format(nrepos))


